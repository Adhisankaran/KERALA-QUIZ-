    fun getFallbackQuizForDay(dayId: Int, count: Int): List<Question> {
        val questions = offlineFallbacks[dayId] ?: generalKeralaGK
        val result = mutableListOf<Question>()
        var tries = 0
        while (result.size < count && tries < 10) {
            val toAdd = questions.map { it.copy(selectedIndex = -1) }.shuffled()
            result.addAll(toAdd)
            tries++
        }
        return result.take(count)
    }
